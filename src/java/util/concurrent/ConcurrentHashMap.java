    /**
     * 【Contended注解是做什么的？】
     * Java8的@sun.misc.Contended注解（Contended：有争议的）
     * @sun.misc.Contended是Java8新增的一个注解，对某字段加上该注解则表示该字段会单独占用一个缓存行（Cache Line）。
     * 这里的缓存行是指CPU缓存（L1、L2、L3）的存储单元，常见的缓存行大小为64字节。
     * （注：JVM添加-XX:-RestrictContended参数后@sun.misc.Contended注解才有效）
     *
     * 单独使用一个缓存行有什么作用？
     * 答：避免伪共享
     *
     * 为了提高读取速度，每个CPU有自己的缓存，CPU读取数据后会存到自己的缓存里。而且为了节省空间，一个缓存行可能存储着多个变量，即伪共享。
     * 但是这对于共享变量，会造成性能问题，如下所示：
     * 当一个CPU要修改某共享变量A时会先锁定自己缓存里A所在的缓存行，并且把其他CPU缓存上相关的缓存行设置为无效。但如果被锁定或失效的缓存行里，还
     * 存储了其他不相干的变量B，其他线程此时就访问不了B，或者由于缓存行失效需要重新从内存中读取加载到缓存里，这就造成了开销。所以让共享变量A单独
     * 使用一个缓存行就不会影响到其他线程的访问。
     *
     * Java8之前的方案是什么？
     * 在Java8之前，是通过代码里手动添加属性的方式解决的，如：
     * class LongWithPadding {
     * 	long value;
     * 	long p0, p1, p2, p3, p4, p5, p6;
     * }
     * 一个long占8个字节，再添加7个long属性就会变成64个字节，刚好是一个缓存行大小。
     * 但是注意，Java7开始JVM做优化时可能会把不用的变量给去掉，所以这种方法并不推荐使用。
     *
     * 适用场景
     * 主要适用于频繁写的共享数据上。如果不是频繁写的数据，那么CPU缓存行被锁的几率就不多，所以没必要使用了，否则不仅占空间还会浪费CPU访问操作数据的时间。
     * ----------------------------------------------------------------------------------------------------------------
     */
    @sun.misc.Contended static final class CounterCell {
        volatile long value;
        CounterCell(long x) {
            value = x;
        }
    }

    /**
     * 统计ConcurrentHashMap包含的所有元素
     * 是由【baseCount】+【counterCells数组中所有的value值之和】计算出来的
     */
    final long sumCount() {
        CounterCell[] as = counterCells;
        CounterCell a;
        long sum = baseCount;
        if (as != null)
            for (int i = 0; i < as.length; ++i)
                if ((a = as[i]) != null)
                    sum += a.value;
        return sum;
    }

    /**
     * 使用CounterCell来计数
     *
     * @param x
     * @param wasUncontended（Uncontended：无竞争） false:表示存在竞争  true:表示不存在竞争
     */
    private final void fullAddCount(long x, boolean wasUncontended) {
        int h;
        /**
         * 1：首先尝试获得随机数h
         * 如果随机数返回0，则进行初始化操作，然后再次获得随机数h
         *
         * 在这里，这个探针哈希值的作用是哈希线程，将线程和数组中的不用元素对应起来，尽量避免线程争用同一数组元素。探针哈希值和map里使用的哈
         * 希值的区别是，当线程发生数组元素争用后，可以改变线程的探针哈希值，让线程去使用另一个数组元素，而map中key对象的哈希值，由于有定位
         * value的需求，所以它是一定不能变的。
         */
        if ((h = ThreadLocalRandom.getProbe()) == 0) {  // 用来获得随机数
            ThreadLocalRandom.localInit();              // 初始化当前线程的探针哈希值
            h = ThreadLocalRandom.getProbe();           // 得到线程的探针哈希值
            wasUncontended = true;
        }

        /**
         * 2：开启无限循环
         */
        boolean collide = false; // collide：碰撞
        for (;;) {
            CounterCell[] as;
            CounterCell a;
            int n;
            long v;
            /** 2-1: counterCells不为空 且 数组长度大于0 */
            if ((as = counterCells) != null && (n = as.length) > 0) {
                /** 2-1-1: 如果随机数h待插入的下标位置是空闲的 */
                if ((a = as[(n - 1) & h]) == null) {
                    /** 2-1-1-1: 当cellsBusy等于0的时候，说明当前没有其他线程操作counterCells */
                    if (cellsBusy == 0) {
                        CounterCell r = new CounterCell(x); // 创建新的ConterCell
                        // 通过CAS将cellsBusy设置为1，表示当前有线程正在操作CounterCell；操作完毕后，会将cellsBusy设置为0，其他线程可以继续操作
                        if (cellsBusy == 0 && U.compareAndSwapInt(this, CELLSBUSY, 0, 1)) {
                            boolean created = false;
                            try {
                                CounterCell[] rs;
                                int m, j;
                                if ((rs = counterCells) != null && (m = rs.length) > 0 && rs[j = (m - 1) & h] == null) {
                                    rs[j] = r; // 没有出现哈希冲突，则在下标j处，插入新建的CounterCell
                                    created = true;
                                }
                            } finally {
                                cellsBusy = 0;  // 将cellsBusy重置为空闲状态
                            }
                            if (created) break; // 插入CounterCell成功，跳出循环
                            continue;
                        }
                    }

                    /** 2-1-1-2: collide:碰撞，cellsBusy不等于0时，表示有其他线程正在操作CounterCell，collide设置为false */
                    collide = false;
                }

                /** 2-1-2: 如果存在线程竞争，则将wasUncontended重置为true */
                else if (!wasUncontended)
                    wasUncontended = true; // 已知CAS失败，重新散列后继续——CAS already known to fail，Continue after rehash

                /** 2-1-3: 如果待插入位置已存在元素a，那么尝试将a的value值加x，此步骤只能有1条线程执行成功，其他线程继续后续else if判断 */
                else if (U.compareAndSwapLong(a, CELLVALUE, v = a.value, v + x))
                    break;

                /** 2-1-4: 如果counterCells已经发生了变化（因为下面会有扩容的情况发生）*/
                else if (counterCells != as || n >= NCPU)
                    collide = false; // 在最大尺寸或陈旧——At max size or stale

                /** 2-1-5: 在上述操作中，多线程之间发生了碰撞，则collide=true */
                else if (!collide)
                    collide = true;

                /**
                 * 2-1-6：多线程之间设置CounterCell的value值时发生碰撞，那么扩展CounterCell的长度，以减少碰撞次数
                 * 即：待插入位置已被a元素占据 且 修改a.value的时候，发生碰撞竞争失败
                 */
                else if (cellsBusy == 0 && U.compareAndSwapInt(this, CELLSBUSY, 0, 1)) {
                    try {
                        if (counterCells == as) {
                            CounterCell[] rs = new CounterCell[n << 1]; // 根据原数组长度扩张2倍
                            for (int i = 0; i < n; ++i) rs[i] = as[i]; // 进行数据迁移
                            counterCells = rs;
                        }
                    } finally {
                        cellsBusy = 0;
                    }
                    collide = false;
                    continue; // Retry with expanded table
                }

                /** 2-1-7：更改当前线程的探针哈希值 */
                h = ThreadLocalRandom.advanceProbe(h);
            }

            /** 2-2: cellsBusy为0且counterCells为空，则创建一个长度为2的CounterCell数组，将x赋值进数组，跳出循环 */
            else if (cellsBusy == 0 && counterCells == as && U.compareAndSwapInt(this, CELLSBUSY, 0, 1)) {
                boolean init = false;
                try { // Initialize table
                    if (counterCells == as) {
                        CounterCell[] rs = new CounterCell[2]; // 创建长度为2的数组
                        rs[h & 1] = new CounterCell(x);
                        counterCells = rs;
                        init = true;
                    }
                } finally {
                    cellsBusy = 0;
                }
                if (init) break;
            }

            /** 2-3: 尝试修改baseCount的值 */
            else if (U.compareAndSwapLong(this, BASECOUNT, v = baseCount, v + x)) break;
        }
    }

