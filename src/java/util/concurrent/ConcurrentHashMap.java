    /* ---------------- Table Initialization and Resizing -------------- */     

    /**
     * 返回用于调整大小为n的表大小的戳位（stamp bits）。当RESIZE_STAMP_SHIFT向左移动时必须为负。
     *
     * Integer.numberOfLeadingZeros(n)：用于标记二进制的table数组长度，从最左边算起连续的“0”的总数量。由于table数组的长度是2的n次方，
     * 所以，可以根据numberOfLeadingZeros方法来判断容量是否相等。
     *
     * (1 << (RESIZE_STAMP_BITS - 1))则表示低16位中，1后面15个0，即：1000 0000 0000 0000
     *
     * 那么resizeStamp返回的结构由三部分组成，就是：
     * 【高16位】16个0
     * 【第16位】1  // 为了当再次左移16位时（因为低16位要存储数据迁移合作线程的总数量），由于最高位是1，所以十进制值永远是负值
     * 【第0~15位】"以二进制对table数组长度进行转换，然后计算从最左边算起连续的“0”的总数量"的二进制表现
     */
    // eg1: n=16=00000000 00000000 00000000 00010000， RESIZE_STAMP_BITS=16
    static final int resizeStamp(int n) {
        return Integer.numberOfLeadingZeros(n) // eg1: 27=00011011
                | (1 << (RESIZE_STAMP_BITS - 1)); // eg1: 00001000000000011011
    }

    /**
     * 使用sizeCtl中记录的大小初始化table数组，如果sizeCtl为-1，则表示table数组正在被别的线程初始化
     */
    private final Node<K,V>[] initTable() {
        Node<K,V>[] tab;
        int sc;
        while ((tab = table) == null || tab.length == 0) {
            /** 1：其他竞争失败的线程，会在while循环中spin，直到table创建完毕才能跳出while循环 */
            if ((sc = sizeCtl) < 0)
                Thread.yield();

            /** 2：如果我们是第一个执行初始化table数组的线程，那么首先通过CAS把sizeCtl设置为-1，表明table正在初始化中…… */
            else if (U.compareAndSwapInt(this, SIZECTL, sc, -1)) {
                try {
                    if ((tab = table) == null || tab.length == 0) {
                        int n = (sc > 0) ? sc : DEFAULT_CAPACITY; // DEFAULT_CAPACITY = 16
                        Node<K,V>[] nt = (Node<K,V>[])new Node<?,?>[n];
                        table = tab = nt;
                        sc = n - (n >>> 2); // sc=n-1/4n=3/4n
                    }
                } finally {
                    sizeCtl = sc; // 用旧值还原sizeCtl
                }
                break;
            }
        }
        return tab;
    }

    /**
     * 总量加1操作
     * 总数=【baseCount】+【counterCells中所有value之和】
     */
    private final void addCount(long x, int check) {
        CounterCell[] as;
        long b, s;

        /** 1：如果此处产生多线程竞争，则只有一条线程可以执行成功，且不用进入if语句中 */
        if ((as = counterCells) != null || // 如果是第1次执行这段代码，则counterCells=null
                !U.compareAndSwapLong(this, BASECOUNT, b = baseCount, s = b + x)) {
            CounterCell a;
            long v;
            int m;
            boolean uncontended = true; // 无竞争
            if (as == null || // 如果counterCells等于null
                    (m = as.length - 1) < 0 || // 如果counterCells的长度减1小于0
                        (a = as[ThreadLocalRandom.getProbe() & m]) == null || // 如果随机一个下标位置，但是没有存储数据
                            !(uncontended = U.compareAndSwapLong(a, CELLVALUE, v = a.value, v + x))) { // 如果多线程竞争失败，则会执行fullAddCount方法
                fullAddCount(x, uncontended); // 使用CounterCell来计数
                return;
            }

            if (check <= 1) return;

            /** 统计所有count值 */
            s = sumCount();
        }


        /** 2：s>=sizeCtl，表示达到了扩容的阈值 */
        if (check >= 0) {
            Node<K,V>[] tab;
            Node<K,V>[] nt;
            int n;
            int sc;
            while (s >= (long)(sc = sizeCtl) &&
                    (tab = table) != null &&
                        (n = tab.length) < MAXIMUM_CAPACITY) {
                // 注意此处bug，应该是：int rs = resizeStamp(n) << RESIZE_STAMP_SHIFT; 请见JDK21
                int rs = resizeStamp(n);

                /** 2-1：sc为负值，表明正在执行扩容操作中 */
                if (sc < 0) {
                    if ((sc >>> RESIZE_STAMP_SHIFT) != rs || // table容量发生了不一致的情况
                            sc == rs + 1 ||
                                sc == rs + MAX_RESIZERS ||
                                    (nt = nextTable) == null ||
                                        transferIndex <= 0)
                        break;

                    if (U.compareAndSwapInt(this, SIZECTL, sc, sc + 1))  // 加入1个共同扩容的线程，即：sc+1
                        transfer(tab, nt);
                }

                /** 2-2：执行扩容操作 */
                else if (U.compareAndSwapInt(this, SIZECTL, sc, (rs << RESIZE_STAMP_SHIFT) + 2))
                    transfer(tab, null);

                /** 统计所有count值 */
                s = sumCount();
            }
        }
    }

    /**
     * 如果正在进行大小调整，则有助于传输。其中：f如果是ForwardingNode类型，则说明该Slot位置已经被处理了
     */
    final Node<K,V>[] helpTransfer(Node<K,V>[] tab, Node<K,V> f) {
        Node<K,V>[] nextTab;
        int sc;
        if (tab != null && // table数组不为null
                (f instanceof ForwardingNode) && // f是ForwardingNode类型
                    (nextTab = ((ForwardingNode<K,V>)f).nextTable) != null) { // f中保存了新的table
            /** 此处有bug，应该是int rs = resizeStamp(tab.length) << RESIZE_STAMP_SHIFT; 参见JDK21 */
            int rs = resizeStamp(tab.length);

            while (nextTab == nextTable && table == tab && (sc = sizeCtl) < 0) { // 表示table正在调整中……
                if ((sc >>> RESIZE_STAMP_SHIFT) != rs ||    // 判断table数组长度是否发生了变化
                        sc == rs + 1 ||                     // 扩容结束了（默认第一个线程设置sc=rs<<16+2，当第一个线程结束扩容了，就会将sc减1，这个时候，sc就等于rs+1了）
                        sc == rs + MAX_RESIZERS ||          // 已经达到最大帮助线程的数量
                                transferIndex <= 0)         // 迁移任务都分配完毕了
                    break;

                // 将本线程加入到扩容大军中，队伍人数+1
                if (U.compareAndSwapInt(this, SIZECTL, sc, sc + 1)) {
                    transfer(tab, nextTab);
                    break;
                }
            }
            return nextTab;
        }
        return table;
    }

    /**
     * 尝试预先调整table数组以容纳给定数量的元素
     */
    private final void tryPresize(int size) {  // 16——>32    00010000——>00100000——>01000000
        /**
         * 1：入参size已经是原长度的2倍了，还会在其基础上，再进行最终长度c的计算，计算方式如下所示：
         * 【如果】size长度超过了最大长度的一半（MAXIMUM_CAPACITY >>> 1），则size的最新长度等于最大长度（MAXIMUM_CAPACITY）
         * 【否则】size的最新长度等于1.5*size+1的最近2的n次幂的值
         */
        int c = (size >= (MAXIMUM_CAPACITY >>> 1)) ? MAXIMUM_CAPACITY : tableSizeFor(size + (size >>> 1) + 1);

        /** 2：当sizeCtl<0时，表示table正在被初始化 或者 正在执行数据迁移操作 */
        int sc;
        while ((sc = sizeCtl) >= 0) {
            Node<K,V>[] tab = table;
            int n;
            /** 2-1：如果table为空表，则创建table数组 */
            if (tab == null || (n = tab.length) == 0) { // 如果tab不为空，则n表示原tab数组长度
                n = (sc > c) ? sc : c;
                if (U.compareAndSwapInt(this, SIZECTL, sc, -1)) { // 将sizeCtl设置为-1，表示table正在被初始化
                    try {
                        if (table == tab) {
                            Node<K,V>[] nt = (Node<K,V>[])new Node<?,?>[n]; // 创建table数组
                            table = nt;
                            sc = n - (n >>> 2); // 0.75n
                        }
                    } finally {
                        sizeCtl = sc; /** 将sizeCtl设置为3/4的table数组长度 */
                    }
                }
            }

            /** 2-2：如果表长度越界，则直接跳出循环 */
            else if (c <= sc || n >= MAXIMUM_CAPACITY)
                break;

            /** 2-3：执行数据迁移操作 */
            else if (tab == table) {
                int rs = resizeStamp(n); // eg1: n=16 rs=1000000000011011
                /** 在后续的JDK版本中，已经删除掉了这段代码。 */
//                if (sc < 0) { // eg1: sc=12
//                    Node<K,V>[] nt;
//                    if ((sc >>> RESIZE_STAMP_SHIFT) != rs || sc == rs + 1 || sc == rs + MAX_RESIZERS ||
//                            (nt = nextTable) == null || transferIndex <= 0) {
//                        break;
//                    }
//                    if (U.compareAndSwapInt(this, SIZECTL, sc, sc + 1)) {
//                        transfer(tab, nt);
//                    }
//                }

                /** (rs << RESIZE_STAMP_SHIFT) + 2所表示含义：
                 * 高16位：记录了旧数组的容量大小信息。
                 * 低16位：保存了参与扩容的线程数量，假设低16位是n，则n-1就是扩容的线程数。
                 * 问题：为什么要+2而不是从1开始计数呢？
                 * 答：因为数组初始化时，sizeCtl设置为-1，所以1的那个位置被占了，所以从2开始计算
                 */
                // else
                if (U.compareAndSwapInt(this, SIZECTL, sc, (rs << RESIZE_STAMP_SHIFT) + 2)) // sizeCtl=-2145714174
                    transfer(tab, null);
            }
        }
    }

    /**
     * 【变量解释】
     * stride：              步长
     * n：                   旧table数组的长度（number）
     * nextn：               新table数组的长度（number）
     * nextTable=nextTab=tn：新的table数组
     * sizeCtl：             下次扩容的阈值，为数组大小的0.75倍
     * transferIndex：       当出现并发扩容时，这个全局变量，是用来给各个线程分配节点的。
     */
    // eg1: nextTab=null
    private final void transfer(Node<K,V>[] tab, Node<K,V>[] nextTab) {
        int n = tab.length; // eg1: n=16
        int stride;
        /**
         * 1：计算步长stride
         * ------------------------------------------------------------
         * 扩容时，计算每次转移的固定节点数(步长)
         * 如果NCPU大于1，则stride=n/8/NCPU，否则stride=n；但是，如果计算出来的stride小于16，那么stride就被赋值为16
         * 其中：最小转移的节点数为：MIN_TRANSFER_STRIDE=16
         */
        if ((stride = (NCPU > 1) ? (n >>> 3) / NCPU : n) < MIN_TRANSFER_STRIDE)
            stride = MIN_TRANSFER_STRIDE; // 16

        /**
         * 2：如果nextTab=null，那么为nextTab创建新的Node数组
         * ------------------------------------------------------------
         * 如果nextTab为空，则创建一个原2倍长度的新数组
         */
        if (nextTab == null) { // eg1: nextTab=null
            try {
                Node<K,V>[] nt = (Node<K,V>[]) new Node<?,?>[n << 1]; // nt=2*n
                nextTab = nt; // 由于nextTab入参为null，所以此处初始化一个nextTab
            } catch (Throwable ex) {
                sizeCtl = Integer.MAX_VALUE;
                return;
            }
            nextTable = nextTab;
            transferIndex = n; // 当出现并发扩容时，这个全局变量，是用来给各个线程分配节点的
        }

        /**
         * 3：开启无限循环，在循环做了两件事
         * ------------------------------------------------------------
         *【1】计算需要转移的节点
         *【2】将每个节点的数据进行转移
         */
        int nextn = nextTab.length;
        ForwardingNode<K,V> fwd = new ForwardingNode<>(nextTab); // 创建hash=MOVED=-1，并且key、value、next都为null的节点
        boolean advance = true; // 推进
        boolean finishing = false;
        for (int i = 0, bound = 0;;) {
            Node<K,V> f;
            int fh;

            /** 3-1：根据步数stride，确定数据迁移下标范围 */
            while (advance) {
                int nextIndex; // 执行初始化nextIndex=0操作，表示下一个索引
                int nextBound; // 执行初始化nextBound=0操作，表示下一个边界

                /** 3-1-1：i执行-1操作，即：往前遍历一个Slot（第一次遍历不会执行）*/
                if (--i >= bound || finishing)
                    advance = false; // 设置跳出while循环标识

                /** 3-1-2：transferIndex<=0，表示数据迁移的活儿都分配完毕了，不需要再划分范围执行迁移了 */
                else if ((nextIndex = transferIndex) <= 0) { // ge1: nextIndex = transferIndex = 32
                    i = -1;
                    advance = false;
                }

                /** 3-1-3：执行迁移范围划分操作。其中：nextBound表示下一个迁移的边界，transferIndex是所有线程共享可见的 */
                else if (U.compareAndSwapInt(this, TRANSFERINDEX, nextIndex, nextBound = (nextIndex > stride ? nextIndex - stride : 0))) {
                    bound = nextBound;  // bound和nextBound用于表示下一组跨度的最大边界值
                    i = nextIndex - 1;  // i表示需要遍历数组的下标，用于下面根据i来迁移对应的链表，因为nextIndex=transferIndex，所以用于倒序遍历的下标
                    advance = false;    // 因为advance=false，所以跳出while循环
                }
            }

            /** 3-2：扩容的活儿都分配完毕了, 如果finishing等于true，则跳出整个无限for循环 */
            if (i < 0 || i >= n || i + n >= nextn) {
                int sc;
                /** 3-2-1：由于默认finishing=false。所以第一次执行这段代码的时候，此处不执行 */
                if (finishing) {
                    nextTable = null;
                    table = nextTab;
                    sizeCtl = (n << 1) - (n >>> 1); // 设置新数组的阈值——sizeCtl=1.5*n=0.75*2n
                    return;
                }

                /** 3-2-2：由于当前线程不需要再执行扩容迁移任务了，所以将总参与数据迁移操作的合作线程数减掉1个 */
                if (U.compareAndSwapInt(this, SIZECTL, sc = sizeCtl, sc - 1)) {
                    // 判断自己是不是最后一个待退出的工作线程。如果不是，则直接return退出即可；如果是最后一个线程，则需要执行收尾工作
                    if ((sc - 2) != resizeStamp(n) << RESIZE_STAMP_SHIFT)
                        return;

                    finishing = advance = true;
                    i = n; //【收尾工作】将遍历下标i又指向了数组尾部n，执行重新检查操作
                }
            }

            /** 3-3：如果【旧table】下标i处没有节点，则不需要进行扩容迁移操作。对【旧table】的位置i赋值fwd，表示该位置已经处理过了，不需要在执行迁移操作了 */
            else if ((f = tabAt(tab, i)) == null) advance = casTabAt(tab, i, null, fwd);

            /** 3-4：下标为i的这个位置已经被处理过了，设置advance=true，重新执行while(advance) */
            else if ((fh = f.hash) == MOVED) advance = true;

            /** 3-5：执行数据迁移操作 */
            else {
                synchronized (f) {
                    if (tabAt(tab, i) == f) {
                        Node<K,V> ln; // 低位的node元素
                        Node<K,V> hn; // 高位的node元素
                        if (fh >= 0) {
                            // n为原table数组长度，当runBit=0时，表明元素f在当前的位置不用移动；否则需要移动到新扩展的区域
                            int runBit = fh & n;

                            // 遍历到链表的最后一个元素，跳出for循环。获得：从链表末尾开始，向前连续的同一区位（runBit）的链表中第一个节点lastRun
                            Node<K,V> lastRun = f;
                            for (Node<K,V> p = f.next; p != null; p = p.next) {
                                int b = p.hash & n;
                                if (b != runBit) {
                                    runBit = b;
                                    lastRun = p;
                                }
                            }

                            // 将lastRun作为低位链表
                            if (runBit == 0) {
                                ln = lastRun;
                                hn = null;
                            }
                            // 将lastRun作为高位链表
                            else {
                                hn = lastRun;
                                ln = null;
                            }

                            // f表示数组下标为i中存放的链表中第一个元素，一直遍历到lastRun节点处
                            for (Node<K,V> p = f; p != lastRun; p = p.next) {
                                int ph = p.hash;
                                K pk = p.key;
                                V pv = p.val;
                                // 采用头插法，将存储在【低位区】的Node拼装成新的链表
                                if ((ph & n) == 0) ln = new Node<K,V>(ph, pk, pv, ln);
                                // 采用头插法，将存储在【高位区】的Node拼装成新的链表
                                else hn = new Node<K,V>(ph, pk, pv, hn);
                            }
                            setTabAt(nextTab, i, ln); /** 设置新table数组的低位链表 */
                            setTabAt(nextTab, i + n, hn); /** 设置新table数组的高位链表 */
                            setTabAt(tab, i, fwd); /** 设置旧table数组i位置为fwd，表示状态为MOVED，即：该下标i处的链表已经迁移完毕 */
                            advance = true;
                        }
                        else if (f instanceof TreeBin) {
                            TreeBin<K,V> t = (TreeBin<K,V>)f;
                            TreeNode<K,V> lo = null, loTail = null;
                            TreeNode<K,V> hi = null, hiTail = null;
                            int lc = 0, hc = 0;
                            for (Node<K,V> e = t.first; e != null; e = e.next) {
                                int h = e.hash;
                                TreeNode<K,V> p = new TreeNode<K,V>
                                    (h, e.key, e.val, null, null);
                                if ((h & n) == 0) {
                                    if ((p.prev = loTail) == null)
                                        lo = p;
                                    else
                                        loTail.next = p;
                                    loTail = p;
                                    ++lc;
                                }
                                else {
                                    if ((p.prev = hiTail) == null)
                                        hi = p;
                                    else
                                        hiTail.next = p;
                                    hiTail = p;
                                    ++hc;
                                }
                            }
                            ln = (lc <= UNTREEIFY_THRESHOLD) ? untreeify(lo) :
                                (hc != 0) ? new TreeBin<K,V>(lo) : t;
                            hn = (hc <= UNTREEIFY_THRESHOLD) ? untreeify(hi) :
                                (lc != 0) ? new TreeBin<K,V>(hi) : t;
                            setTabAt(nextTab, i, ln);
                            setTabAt(nextTab, i + n, hn);
                            setTabAt(tab, i, fwd);
                            advance = true;
                        }
                    }
                }
            }
        }
    }

