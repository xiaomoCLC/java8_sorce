    // Unsafe mechanics
    private static final sun.misc.Unsafe U;
    private static final long SIZECTL;
    private static final long TRANSFERINDEX;
    private static final long BASECOUNT;
    private static final long CELLSBUSY;
    private static final long CELLVALUE;
    private static final long ABASE;
    private static final int ASHIFT;

    static {
        try {
            U = sun.misc.Unsafe.getUnsafe();
            Class<?> k = ConcurrentHashMap.class;
            SIZECTL = U.objectFieldOffset(k.getDeclaredField("sizeCtl"));
            TRANSFERINDEX = U.objectFieldOffset(k.getDeclaredField("transferIndex"));
            BASECOUNT = U.objectFieldOffset(k.getDeclaredField("baseCount"));
            CELLSBUSY = U.objectFieldOffset(k.getDeclaredField("cellsBusy"));
            Class<?> ck = CounterCell.class;
            CELLVALUE = U.objectFieldOffset(ck.getDeclaredField("value"));
            Class<?> ak = Node[].class;
            /**
             * 数组对象：对象头（8字节）+ 指针（4字节）+ 数组长度（4字节）=16，所以基础偏移量为16
             *
             * public native int arrayBaseOffset(Class<?> arrayClass);
             * 返回数组类型的【第一个元素】的偏移地址（基础偏移地址）。
             * 如果arrayIndexScale方法返回的比例因子不为0，你可以通过结合基础偏移地址和比例因子访问数组的所有元素。
             */
            ABASE = U.arrayBaseOffset(ak); // 16
            /** 返回数组类型的【比例因子】(其实就是【数据中元素偏移地址的增量】，因为数组中的元素的地址是连续的) */
            int scale = U.arrayIndexScale(ak); // 4
            if ((scale & (scale - 1)) != 0) { // 4 & (4-1) = 0100 & 0011 = 0000
                throw new Error("data type scale not a power of two");
            }
            /**
             * Integer.numberOfLeadingZeros(int i)：给定一个int类型数据，返回这个数据的二进制串中从最左边算起连续的“0”的总数量。
             * 而ASHIFT其实就是将scale数值转换为【按位左移对应的数值】，即：通过scale=4，那么计算出ASHFIT=2，而N<<2其实就相当于N*2*2=N*4=N*scale
             * 举例：
             *  如果scale=8（十进制）=1000（二进制）
             *  那么计算出Integer.numberOfLeadingZeros(scale)=28
             *  ASHIFT=31-28=3
             *  N<<3=N*2*2*2=N*8=N*scale
             */
            ASHIFT = 31 - Integer.numberOfLeadingZeros(scale); // ASHIFT=31-29=2
        } catch (Exception e) {
            throw new Error(e);
        }
    }
